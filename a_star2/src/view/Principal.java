    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //lendo os parametros
        int miss1 = (Integer) spnCan1.getValue();
        int can1 = (Integer) spnCan1.getValue();
        boolean margen1 = ((String) jcbMargem1.getSelectedItem()).equalsIgnoreCase("Esquerda") ? false : true;
//        int barco1 = cbBarco1.isSelected() ? 1 : 0;

        int miss2 = (Integer) spnCan2.getValue();
        int can2 = (Integer) spnCan2.getValue();
        boolean margen2 = ((String) jcbMargem2.getSelectedItem()).equalsIgnoreCase("Esquerda") ? false : true;
//        int barco2 = cbBarco2.isSelected() ? 1 : 0;

        ArrayList<Impressao> lista = new ArrayList<>();
        Operacao op = new Operacao();
        ArrayList<Estado> todosEstados
                = op.processar(
                        //                        new Estado("inicio", 3, 3, false, 1),
                        //                        new Estado("fim", 0, 0, true, 0)
                        new Estado("inicio", miss1, can1, margen1, 1),
                        new Estado("fim", miss2, can2, margen2, 0)
                );
        if (todosEstados.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ocorreu algum erro! \n Reinicie a aplicacao...");
        } else {
            for (int i = 0; i < todosEstados.size(); i++) {
                Estado s = todosEstados.remove(0);
                s.gerarSolucao(lista);
            }
        }
        this.preencherEstados(lista);
    }//GEN-LAST:event_jButton1ActionPerformed

