    public JPanel pnlEstado(Impressao imp) {
        JPanel a = new JPanel();
        a.setLayout(new GridLayout(1, 3));
        //buscar os dados em imp 
        a.add(this.preencherLado(imp.getCanEsquerda(), imp.getMissEsquerda()));
        a.add(this.preencherRio(imp.isDireita()));
        a.add(this.preencherLado(imp.getCanDireita(), imp.getMissDireita()));
        a.setBackground(Color.white);
        a.setBorder(new EtchedBorder(2, Color.white, Color.pink));
        return a;
    }

    public JLabel preencherRio(boolean dir) {
        return (!dir) ? new JLabel(new ImageIcon(this.getClass().getResource("imagens/barco_a_direita.png"))) : new JLabel(new ImageIcon(this.getClass().getResource("imagens/barco_a_esquerda.png")));
    }

    public JPanel preencherLado(int can, int miss) {
        JPanel a = new JPanel();
        for (int i = 0; i < miss; i++) {
            a.add(new JLabel(new ImageIcon(this.getClass().getResource("imagens/missionario.png"))));
        }
        for (int i = 0; i < can; i++) {
            a.add(new JLabel(new ImageIcon(this.getClass().getResource("imagens/canibal.png"))));
        }
        a.setBackground(Color.white);
        return a;
    }

    public void preencherEstados(ArrayList<Impressao> lista) {
        this.pnlBase.removeAll();
        for (Impressao imp : lista) {
            this.pnlBase.repaint();
            pnlBase.add(this.pnlEstado(imp));
            this.pnlBase.revalidate();
        }
    }

